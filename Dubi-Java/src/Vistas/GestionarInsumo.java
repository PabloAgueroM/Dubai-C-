    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        Insumo i = new Insumo();
        i.setNombreInsumo(txtNombre.getText());
        i.setDescInsumo(txtDescripcion.getText());
        i.setStock(Double.parseDouble(txtStock.getText()));
        i.setStockMin(Double.parseDouble(txtStockMinimo.getText()));
        LogicaNegocio.registrarInsumo(i);
        txtNombre.setText(null);
        txtDescripcion.setText(null);
        txtStock.setText(null);
        txtStockMinimo.setText(null);
    }//GEN-LAST:event_btnAgregarMouseClicked

    private void btnBuscarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnBuscarMouseClicked
        // TODO add your handling code here:
        frmBusquedaInsumo frm = new frmBusquedaInsumo(null,true);
        frm.setVisible(true);
        insumoSeleccionado = frm.getInsumoSeleccionado();
        if(insumoSeleccionado != null){
            txtNombre.setText(insumoSeleccionado.getNombreInsumo());
            txtDescripcion.setText(insumoSeleccionado.getDescInsumo());
            txtStock.setText(Double.toString(insumoSeleccionado.getStock()));
            txtStockMinimo.setText(Double.toString(insumoSeleccionado.getStockMinimo()));

            btnAgregar.setEnabled(false);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);  
        }
      
    }//GEN-LAST:event_btnBuscarMouseClicked

    private void btnModificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnModificarMouseClicked
        // TODO add your handling code here:
        Insumo i = new Insumo();
        i.setIdInsumo(insumoSeleccionado.getIdInsumo());
        i.setNombreInsumo(txtNombre.getText());
        i.setDescInsumo(txtDescripcion.getText());
        i.setStock(Double.parseDouble(txtStock.getText()));
        i.setStockMin(Double.parseDouble(txtStockMinimo.getText()));
        LogicaNegocio.modificarInsumo(i);
        insumoSeleccionado = null;

        txtNombre.setText(null);
        txtDescripcion.setText(null);
        txtStock.setText(null);
        txtStockMinimo.setText(null);
        
    }//GEN-LAST:event_btnModificarMouseClicked

    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        // TODO add your handling code here:
        LogicaNegocio.eliminarInsumo(insumoSeleccionado);
        insumoSeleccionado = null;
        txtNombre.setText(null);
        txtDescripcion.setText(null);
        txtStock.setText(null);
        txtStockMinimo.setText(null);
    }//GEN-LAST:event_btnEliminarMouseClicked

