    public ModificarProveedor(ArrayList<Proveedor> _proveedores) throws Exception {
        initComponents();
        proveedores = _proveedores;
        DefaultTableModel modelo = (DefaultTableModel) TablaModificar.getModel();
        Object[] fila = new Object[5];
        for (int i = 0; i < proveedores.size(); i++) {
            fila[0] = proveedores.get(i).getRUC();
            fila[1] = proveedores.get(i).getNombre();
            fila[2] = proveedores.get(i).getRepresentante().getNombres();
            fila[3] = proveedores.get(i).getRepresentante().getApellidoP();
            fila[4] = proveedores.get(i).getRepresentante().getTelefono();
            modelo.addRow(fila);
        }
    }

    public Proveedor devolverProveedor() {
        return this.ProveedorSeleccionado;
    }

