    public void actualizarDatos(){
        DefaultTableModel modelo = (DefaultTableModel) tblInsumos.getModel();
        Object[] fila = new Object[4];
        for(int i=0;i<listaInsumos.size();i++){
            fila[0]= listaInsumos.get(i).getNombreInsumo();
            fila[1]= listaInsumos.get(i).getDescInsumo();
            fila[2]= listaInsumos.get(i).getStock();
            fila[3]= listaInsumos.get(i).getStockMinimo();
            modelo.addRow(fila);
        }  
    }
