    public void ActualizarTablaP(){
        DefaultTableModel modelo = (DefaultTableModel) TablaProductos.getModel();
        Object[] filaP = new Object[4];
        int n=modelo.getRowCount();
        for(int i=0; i<n;i++) modelo.removeRow(0);
        for(int i=0; i<listaProductos.size();i++){
            if (listaProductos.get(i).getEstado()==Estado.Activo){
                filaP[0] = listaProductos.get(i).getID();
                filaP[1] = listaProductos.get(i).getDescripcion();
                filaP[2] = listaProductos.get(i).getPrecio();
                filaP[3] = listaProductos.get(i).getStock();
                modelo.addRow(filaP);
            }
        }
    }
